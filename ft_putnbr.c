#include "libft.h"

void	ft_putnbr(int n)
{
	long long int num;

	num = (long long int)n;
	if (num < 0)
	{
		ft_putchar('-');
		num = num * -1;
	}
	if (num / 10 != 0)
		ft_putnbr(num / 10);
	if (((num % 10) >= '0') || (num % 10) <= '9')
		ft_putchar(num % 10 + '0');
}

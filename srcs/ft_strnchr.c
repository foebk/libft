#include "libft.h"

int	ft_strnchr(const char *str, int ch)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
		i++;
	while (i != -1)
	{
		if (str[i] == ch)
			return (i);
		i--;
	}
	return (-1);
}

#include "libft.h"

void	*ft_memset(void *dest, int c, size_t n)
{
	unsigned int i;

	i = 0;
	while (i != n)
	{
		((char *)dest)[i] = (unsigned char)c;
		i++;
	}
	return (dest);
}
